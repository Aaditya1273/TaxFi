"""
Tests for TaxFi PDF generation.

Verifies that ReportLab generates valid PDFs for:
- Form 8949 (short-term and long-term transactions)
- Schedule D (capital gains summary)
- Tax summary (plain-English with onchain hashes)
"""

from __future__ import annotations

import pytest


def test_generate_form_8949_pdf():
    """Form 8949 PDF should be generated with valid PDF structure."""
    from backend.utils.pdf_generator import generate_form_8949

    short_term = [
        {
            "description": "0.5 ETH",
            "acquisition_date": "2025-01-15",
            "sale_date": "2025-06-01",
            "proceeds": 1500.00,
            "cost_basis": 1000.00,
            "gain_loss": 500.00,
        },
    ]

    long_term = [
        {
            "description": "1.0 BTC",
            "acquisition_date": "2023-01-10",
            "sale_date": "2025-08-01",
            "proceeds": 60000.00,
            "cost_basis": 30000.00,
            "gain_loss": 30000.00,
        },
    ]

    pdf_bytes = generate_form_8949(
        user_address="0x742d35Cc6634C0532925a3b844Bc9e7595f2bD18",
        tax_year=2025,
        short_term_entries=short_term,
        long_term_entries=long_term,
    )

    assert len(pdf_bytes) > 500, "PDF should have substantial content"
    assert pdf_bytes.startswith(b"%PDF"), "Should start with PDF magic bytes"
    assert b"ReportLab" in pdf_bytes, "Should be generated by ReportLab"
    # Note: PDF content is compressed by ReportLab, so text assertions check the magic header only


