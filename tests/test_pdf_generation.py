def test_generate_all_forms():
    """generate_all_forms should return a dict of valid PDFs."""
    from backend.utils.pdf_generator import generate_all_forms

    form_data = {
        "form_8949": {
            "parts": {
                "I": {"transactions": []},
                "II": {"transactions": []},
            }
        },
        "schedule_d": {
            "part_i": {"total_gain": 5000.0, "total_loss": 1000.0},
            "part_ii": {"total_gain": 30000.0, "total_loss": 2000.0},
        },
        "summary": {
            "summary": "Your 2025 tax summary.",
            "key_numbers": {"net_capital_gain": "$32,000"},
        },
    }

    onchain_hashes = {"form_8949": "0xabc123"}

    pdfs = generate_all_forms(
        user_address="0x742d35Cc6634C0532925a3b844Bc9e7595f2bD18",
        tax_year=2025,
        form_data=form_data,
        onchain_hashes=onchain_hashes,
    )

    assert "form_8949.pdf" in pdfs
    assert "schedule_d.pdf" in pdfs
    assert "tax_summary.pdf" in pdfs

    for name, content in pdfs.items():
        assert len(content) > 300, f"{name} should have content"
        assert content.startswith(b"%PDF"), f"{name} should be a valid PDF"
